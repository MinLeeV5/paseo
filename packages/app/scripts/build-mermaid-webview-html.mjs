import esbuild from "esbuild";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(appRoot, "../..");
const entry = path.join(appRoot, "src/components/mermaid/webview/mermaid-webview-entry.ts");
const output = path.join(appRoot, "src/components/mermaid/webview/mermaid-webview-html.ts");

const result = await esbuild.build({
  entryPoints: [entry],
  bundle: true,
  write: false,
  format: "iife",
  platform: "browser",
  target: ["ios15", "chrome100"],
  logLevel: "info",
});

const js = result.outputFiles[0]?.text;
if (!js) {
  throw new Error("mermaid webview bundle produced no JavaScript");
}

const html = `<!doctype html>
<html>
  <head>
    <meta charset="utf-8" />
    <meta
      name="viewport"
      content="width=device-width, initial-scale=1, maximum-scale=1, user-scalable=no, viewport-fit=cover"
    />
  </head>
  <body>
    <script>${js}</script>
  </body>
</html>`;

const contents = `// Generated by packages/app/scripts/build-mermaid-webview-html.mjs.
// Do not edit by hand.

export const mermaidWebViewHtml = ${JSON.stringify(html)};
`;

await fs.mkdir(path.dirname(output), { recursive: true });
await fs.writeFile(output, contents);
console.log(`Wrote ${path.relative(repoRoot, output)} (${html.length} bytes)`);
